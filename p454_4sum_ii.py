class Solution(object):
    def fourSumCount(self, A, B, C, D):
        """
        :type A: List[int]
        :type B: List[int]
        :type C: List[int]
        :type D: List[int]
        :rtype: int
        """
        from collections import defaultdict
        sum_ab = defaultdict(int)
        for a in A:
            for b in B:
                sum_ab[a + b] += 1
        counts = 0
        for c in C:
            for d in D:
                counts += sum_ab[-c - d]
        return counts


